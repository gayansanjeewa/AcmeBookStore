namespace App\Repository;

use App\Application\Contract\BookRepositoryInterface;
use App\Entity\Book;
use Doctrine\Bundle\DoctrineBundle\Repository\ServiceEntityRepository;
use Doctrine\Persistence\ManagerRegistry;

/**
 * @method Book|null find($id, $lockMode = null, $lockVersion = null)
 * @method Book|null findOneBy(array $criteria, array $orderBy = null)
 * @method Book[]    findAll()
 * @method Book[]    findBy(array $criteria, array $orderBy = null, $limit = null, $offset = null)
 */
class BookRepository extends ServiceEntityRepository implements BookRepositoryInterface
{
    public function __construct(ManagerRegistry $registry)
    {
        parent::__construct($registry, Book::class);
    }

    /**
     * @{@inheritdoc}
     */
    public function findByCategoryCriteria(array $criteria, array $orderBy = null, $limit = null, $offset = null): array
    {
        return $this->createQueryBuilder('book')
            ->innerJoin('book.category', 'category')
            ->andWhere('category.uuid IN (:val)')
            ->setParameter('val', $criteria['category'])
            ->orderBy('book.id', 'ASC')
            ->setMaxResults(10)
            ->getQuery()
            ->getResult();
    }

